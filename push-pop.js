//Métodos Push e Pop
//Push adiciona um novo item ao final da array
//Pop remove o último item do array

const nomesDeEstudantes = ['jose', 'joao', 'maria', 'joana', 'andreia', 'alice'];

nomesDeEstudantes.push('pedro');
nomesDeEstudantes.pop();
nomesDeEstudantes.pop();
nomesDeEstudantes.pop();
nomesDeEstudantes.pop();
nomesDeEstudantes.pop();
nomesDeEstudantes.pop();

